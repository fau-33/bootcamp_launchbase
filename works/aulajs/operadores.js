/*====================================
  OPERADORES DE COMPARAÇÃO

  >   Maior
  <   Menor
  >=  Maior igual a
  <=  Menor igual a
  ==  Igual a 
  === Igual e do mesmo tipo
  !=  Diferente de
  !== Diferente, inclusive do tipo 

===================================== */

console.log(4 == "4")// true
console.log(4 === "4")// false
console.log(4 != "5")// true
console.log(4 !== "5")// true