/*====================================
  OPERADORES DE COMPARAÇÃO

  >   Maior
  <   Menor
  >=  Maior igual a
  <=  Menor igual a
  ==  Igual a 
  === Igual e do mesmo tipo
  !=  Diferente de
  !== Diferente, inclusive do tipo 

===================================== */

console.log(5 > 4)// true
console.log(5 < 4)// false
console.log(5 >= 4)// true
console.log(4 <= 4)// true